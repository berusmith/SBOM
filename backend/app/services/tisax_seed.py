def make_controls(assessment_id: str, module: str) -> list[dict]:
    """Return a list of TISAXControl field dicts for the given module."""
    if module == "infosec":
        items = INFOSEC_CONTROLS
    elif module == "prototype":
        items = PROTOTYPE_CONTROLS
    else:
        items = DATA_PROTECTION_CONTROLS
    import uuid as _uuid
    return [
        {
            "id":                  str(_uuid.uuid4()),
            "assessment_id":       assessment_id,
            "control_number":      num,
            "chapter":             chapter,
            "name":                name,
            "requirement_summary": req,
            "module":              module,
            "current_maturity":    0,
            "target_maturity":     3,
            "status":              "unassessed",
            "evidence_note":       None,
            "owner":               None,
            "due_date":            None,
            "remarks":             None,
        }
        for num, chapter, name, req in items
    ]
